local function combatSpell(c,cid,var,dirList,dirEmitPos)
	if (isCreature(cid)) then
		doCombat(cid, c, var)
		if (dirList ~= nil and dirEmitPos ~= nil) then
			local i = 2;
			while (i < #dirList) do
				doSendDistanceShoot(dirEmitPos,{x=dirEmitPos.x-dirList[i],y=dirEmitPos.y-dirList[i+1],z=dirEmitPos.z},dirList[1])
				i = i + 2
			end		
		end
	end
end

function onCastSpell(creature, var)
	local cid = creature:getId()

	if isCreature(cid) then
		creature:say("We are ... freezing!", TALKTYPE_MONSTER_SAY)
		creature:setNoMove(true)
	end
	addEvent(function()
	addEvent(combatSpell,300,combat1,cid,var)
	addEvent(combatSpell,500,combat2,cid,var)
	addEvent(combatSpell,700,combat3,cid,var)
	addEvent(combatSpell,900,combat4,cid,var)
	addEvent(combatSpell,1100,combat5,cid,var)
	addEvent(combatSpell,1300,combat6,cid,var)
	addEvent(combatSpell,1500,combat7,cid,var)
	addEvent(combatSpell,1700,combat8,cid,var)
	addEvent(combatSpell,1900,combat9,cid,var)
	addEvent(function()
        if isCreature(cid) then
            creature:setNoMove(false)
        end
    end, 2200)
	end, 2000)
	return true
end